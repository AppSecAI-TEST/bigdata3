package step07.ex3;

public class Calculator {
  
  static float plus(float a, float b) {
    return a + b;
  }
  
  static float minus(float a, float b) {
    return a - b;
  }
  
  static float multiple(float a, float b) {
    return a * b;
  }
  
  static float divide(float a, float b) {
    return a / b;
  }
}
